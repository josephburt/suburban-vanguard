function processEndOfWeek() {
    printSystem("--- WEEKLY BUDGET DEDUCTIONS ---");
    const inflation = 1 + ((state.time.month - 1) * 0.2);
    const expenses = [
        { name: "Mortgage/Rent", cost: Math.floor(412 * inflation) },
        { name: "Groceries", cost: Math.floor(212 * inflation) },
        { name: "Healthcare Premium", cost: Math.floor(120 * inflation) },
        { name: "Gas & Insurance", cost: Math.floor(95 * inflation) }
    ];
    let total = 0;
    expenses.forEach(exp => {
        printSystem(`Paid: ${exp.name} (-$${exp.cost})`);
        total += exp.cost;
    });
    state.player.finances -= total;
    printAction(`Total Weekly Expenses: -$${total}.`);
    
    if (state.player.finances < 0) {
        playBuzzer();
        printError("YOU ARE OVERDRAWN. The Stress is immense.");
        adjustStat('sanity', -30);
        addAlert("ACCOUNT OVERDRAWN.");
    } else {
        addScore(100, "Survived the week solvent");
    }
}

