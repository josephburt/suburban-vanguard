function spendTU(amount) {
    if (state.time.tu <= 0) {
        printError("You are exhausted. You have 0 Time Units left. You must go to the bedroom and 'sleep'.");
        return false;
    }
    
    state.time.tu -= amount;
    if (state.time.tu <= 0) state.time.tu = 0;
    
    let previousPhase = state.time.phase;
    if (state.time.tu <= 6) state.time.phase = 'Evening Phase';
    else if (state.time.tu <= 18) state.time.phase = 'Core Learning/Work Phase';
    
    if (previousPhase !== state.time.phase) {
        printSystem(`--- Transitioning to ${state.time.phase} ---`);
        look();
    }
    
    state.linda.burnout = Math.min(100, state.linda.burnout + (amount * 0.5));
    
    updateUI();
    return true;
}

function adjustStat(stat, amount) {
    state.player[stat] = Math.max(0, Math.min(100, state.player[stat] + amount));
    
    if (stat === 'sanity' && state.player.sanity <= 0) {
        playBuzzer();
        printError("STRESS EXPLOSION! You couldn't handle the pressure and snapped at your family.");
        state.player.harmony -= 20;
        state.player.sanity = 30; 
        addAlert("Stress Explosion: -20 Harmony");
    }
    
    if (stat === 'harmony' && state.player.harmony <= 0) {
        playBuzzer();
        printError("GAME OVER. Family Harmony has collapsed completely.");
        addAlert("GAME OVER: Divorce / Total Breakdown.");
        triggerGameOver();
    }
    
    updateUI();
}

