function init() {
    printIntro();
    look();
    updateUI();
    
    // Replace input to clear old listeners if restarting
    const newTarget = inputEl.cloneNode(true);
    inputEl.parentNode.replaceChild(newTarget, inputEl);
    
    const newInputEl = document.getElementById('cmd-input');
    
    newInputEl.addEventListener('input', () => {
        const val = newInputEl.value.toLowerCase();
        if (!val) {
            suggestionEl.value = '';
            return;
        }
        const match = validCommands.find(c => c.startsWith(val));
        if (match) {
            suggestionEl.value = newInputEl.value + match.slice(val.length);
        } else {
            suggestionEl.value = '';
        }
    });

    newInputEl.addEventListener('keydown', (e) => {
        initAudio(); // Boot up audio context on first interaction
        
        if (e.key !== 'Enter' && e.key !== 'Tab' && e.key !== 'ArrowRight' && e.key !== 'Shift' && e.key !== 'Control') {
            playKeystroke();
        }

        if (e.key === 'Tab' || e.key === 'ArrowRight') {
            if (suggestionEl.value && suggestionEl.value !== newInputEl.value) {
                e.preventDefault();
                newInputEl.value = suggestionEl.value;
                suggestionEl.value = '';
            }
        } else if (e.key === 'Enter') {
            const cmd = newInputEl.value.trim();
            if (cmd) {
                processCommand(cmd);
                newInputEl.value = '';
                suggestionEl.value = '';
            }
        }
    });
    newInputEl.focus();
}

function getClockTimeStr() {
    const elapsedMinutes = Math.floor((21 - state.time.tu) * 60);
    const totalMinutesFromMidnight = (6 * 60) + elapsedMinutes;
    const hours24 = Math.floor(totalMinutesFromMidnight / 60) % 24;
    const minutes = totalMinutesFromMidnight % 60;
    const ampm = hours24 >= 12 ? 'PM' : 'AM';
    let hours12 = hours24 % 12;
    if (hours12 === 0) hours12 = 12;
    return `${hours12}:${minutes.toString().padStart(2, '0')} ${ampm}`;
}

